// now we want to standardize our error message . as a result everytime when we are getting error then there remains
// consistency , as as result we will be using JS libraries to overWrite the error mesages by using classes and 
// inheritance 

class ApiError extends Error {
    constructor(
        statusCode,
        message= "Something went wrong",
        errors = [],
        stack = ""
    ){
        super(message)
        this.statusCode = statusCode
        this.data = null
        this.message = message
        this.success = false;
        this.errors = errors

        if (stack) {
            this.stack = stack
        } else{
            Error.captureStackTrace(this, this.constructor)
        }

    }
}

export {ApiError}